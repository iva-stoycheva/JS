// A variable is a named container used to store data. 
// It allows us to save values and use them later in our program.

// We use const when we don't want the value to be reassigned.
const firstName = "Iva";

// We use let because the value may change later.
let age = 25;
age = 26;

const city = "Stara Zagora";

let isStudent = true;

console.log(firstName);
console.log(age);
console.log(city);
console.log(isStudent);